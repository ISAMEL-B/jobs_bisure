<?php

/*
|--------------------------------------------------------------------------
| START SESSION
|--------------------------------------------------------------------------
*/

session_start();

/*
|--------------------------------------------------------------------------
| UNSET ALL SESSION VARIABLES
|--------------------------------------------------------------------------
*/

$_SESSION = [];

/*
|--------------------------------------------------------------------------
| DESTROY SESSION COOKIE
|--------------------------------------------------------------------------
*/

if (ini_get("session.use_cookies")) {

    $params = session_get_cookie_params();

    setcookie(
        session_name(),
        '',
        time() - 42000,
        $params["path"],
        $params["domain"],
        $params["secure"],
        $params["httponly"]
    );
}

/*
|--------------------------------------------------------------------------
| DESTROY CUSTOM REMEMBER ME COOKIE (OPTIONAL)
|--------------------------------------------------------------------------
*/

if (isset($_COOKIE['remember_token'])) {

    setcookie(
        'remember_token',
        '',
        time() - 3600,
        '/'
    );
}

/*
|--------------------------------------------------------------------------
| DESTROY SESSION
|--------------------------------------------------------------------------
*/

session_destroy();

/*
|--------------------------------------------------------------------------
| REDIRECT TO LOGIN
|--------------------------------------------------------------------------
*/

header("Location: signin.php");

exit;
?>